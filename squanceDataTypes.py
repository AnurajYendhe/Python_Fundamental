No = None  # None
print(No)
print(type(No))
print(id(No))

No = 10  # integer
print(No)
print(type(No))
print(id(No))

No = 3.14 # float
print(No)
print(type(No))
print(id(No))

No = True # boolean
print(No)
print(type(No))
print(id(No))

No = "Anuraj" # string
print(No)
print(type(No))
print(id(No))
